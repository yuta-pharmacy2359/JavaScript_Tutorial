var code = 3;
switch(code) {
  case 1 :
    console.log('りんご');
    break;
  case 2 :
    console.log('ぶどう');
    break;
  case 3 :
    console.log('バナナ');
    break;
  case 4 :
    console.log('みかん');
    break;
  case 5 :
    console.log('スイカ');
    break
  default :
    console.log('1〜5の数字を入力してください。');
    break;
}

